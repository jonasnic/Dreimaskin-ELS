def write_header_file() -> Path:
    if len(patterns) != 64:
        raise ValueError(f"Expected 64 patterns, got {len(patterns)}")

    lines = [
        "#pragma once",
        "",
        "#include <stdint.h>",
        "",
        "constexpr uint64_t kBitPatterns[64] = {",
    ]

    for value in patterns:
        lines.append(f"    0x{value:016x}ULL,")

    lines.extend([
        "};",
        "",
    ])

    output_path = Path(__file__).resolve().parent.parent / "include" / "bitpattern_table.h"
    output_path.write_text("\n".join(lines), encoding="utf-8")
    return output_path


